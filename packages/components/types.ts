/**
 * 组件库公共类型定义
 */

/**
 * 组件尺寸
 * @description sm - 小型 | md - 中型 | lg - 大型
 */
export type ComponentSize = 'sm' | 'md' | 'lg'

/**
 * 组件类型
 * @description primary - 主要 | default - 默认 | success - 成功 | warning - 警告 | danger - 危险
 */
export type ComponentType = 'primary' | 'default' | 'success' | 'warning' | 'danger'

/**
 * 组件状态
 * @description success - 成功 | warning - 警告 | error - 错误 | info - 信息
 */
export type ComponentStatus = 'success' | 'warning' | 'error' | 'info'
